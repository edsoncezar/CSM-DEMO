package br.com.alcatellucent.csm.exception;

public class ExceptionSys extends Exception {
    
    private static final long serialVersionUID = -6729264401632167144L;
    
    private static final String message = "System Error: ";

    public ExceptionSys() {
	super(message);
    }

    public ExceptionSys(String msg) {
	super(message + ": " + msg);
    }

    public ExceptionSys(Throwable t) {
	super(t);
    }

    public ExceptionSys(String msg, Throwable t) {
	super(message + ": " + msg, t);
    }
    
}