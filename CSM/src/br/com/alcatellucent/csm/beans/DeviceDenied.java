package br.com.alcatellucent.csm.beans;

public class DeviceDenied {
	
	
	private String prfid;
	private String csmdeviceId;
	private String userProfileId;
	/**
	 * @return the prfid
	 */
	public String getPrfid() {
		return prfid;
	}
	/**
	 * @param prfid the prfid to set
	 */
	public void setPrfid(String prfid) {
		this.prfid = prfid;
	}
	/**
	 * @return the csmdeviceId
	 */
	public String getCsmdeviceId() {
		return csmdeviceId;
	}
	/**
	 * @param csmdeviceId the csmdeviceId to set
	 */
	public void setCsmdeviceId(String csmdeviceId) {
		this.csmdeviceId = csmdeviceId;
	}
	
	/**
	 * @return the userProfileId
	 */
	public String getUserProfileId() {
		return userProfileId;
	}
	/**
	 * @param userProfileId the userProfileId to set
	 */
	public void setUserProfileId(String userProfileId) {
		this.userProfileId = userProfileId;
	}
	
	

}
